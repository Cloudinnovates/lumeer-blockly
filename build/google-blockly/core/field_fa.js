'use strict';

goog.provide('Blockly.FieldFA');

goog.require('Blockly.Field');
goog.require('Blockly.Tooltip');
goog.require('Blockly.utils');

goog.require('goog.math.Size');


/**
 * Class for a non-editable field.
 * @param {string} text The initial content of the field.
 * @param {string=} opt_class Optional CSS class for the field's text.
 * @extends {Blockly.Field}
 * @constructor
 */
Blockly.FieldFA = function(icon, iconColor) {
  this.size_ = new goog.math.Size(0, 17.5);
  this.icon_ = this.ICONS.get(icon);
  if (!this.icon_) {
    this.icon_ = "";
  }
  this.iconColor_ = iconColor;
  this.iconStyle_ = icon.split(' ')[0];
  this.setValue(this.icon_);
  this.tooltip_ = '';
};
goog.inherits(Blockly.FieldFA, Blockly.Field);

/**
 * Construct a FieldLabel from a JSON arg object,
 * dereferencing any string table references.
 * @param {!Object} options A JSON object with options (text, and class).
 * @returns {!Blockly.FieldLabel} The new field instance.
 * @package
 * @nocollapse
 */
Blockly.FieldFA.fromJson = function(options) {
  return new Blockly.FieldFA(options['icon'], options['iconColor']);
};

/**
 * Editable fields are saved by the XML renderer, non-editable fields are not.
 */
Blockly.FieldFA.prototype.EDITABLE = false;

/**
 * Install this text on a block.
 */
Blockly.FieldFA.prototype.init = function() {
  if (this.textElement_) {
    // Text has already been initialized once.
    return;
  }
  // Build the DOM.
  this.textElement_ = Blockly.utils.createSvgElement('text',
      {'class': 'blocklyText',
          'y': this.size_.height - 5}, null);
  Blockly.utils.addClass(this.textElement_, 'blocklyFontAwesome');
  Blockly.utils.addClass(this.textElement_, this.iconStyle_);
  this.textElement_.style.fill = this.iconColor_;
  if (!this.visible_) {
    this.textElement_.style.display = 'none';
  }
  this.sourceBlock_.getSvgRoot().appendChild(this.textElement_);

  if (this.tooltip_) {
    this.textElement_.tooltip = this.tooltip_;
  } else {
    // Configure the field to be transparent with respect to tooltips.
    this.textElement_.tooltip = this.sourceBlock_;
  }
  Blockly.Tooltip.bindMouseEvents(this.textElement_);
  // Force a render.
  this.render_();
};

/**
 * Dispose of all DOM objects belonging to this text.
 */
Blockly.FieldFA.prototype.dispose = function() {
  if (this.textElement_) {
    Blockly.utils.removeNode(this.textElement_);
    this.textElement_ = null;
  }
};

/**
 * Gets the group element for this field.
 * Used for measuring the size and for positioning.
 * @return {!Element} The group element.
 */
Blockly.FieldFA.prototype.getSvgRoot = function() {
  return /** @type {!Element} */ (this.textElement_);
};

/**
 * Change the tooltip text for this field.
 * @param {string|!Element} newTip Text for tooltip or a parent element to
 *     link to for its tooltip.
 */
Blockly.FieldFA.prototype.setTooltip = function(newTip) {
  this.tooltip_ = newTip;
  if (this.textElement_) {
    this.textElement_.tooltip = newTip;
  }
};

